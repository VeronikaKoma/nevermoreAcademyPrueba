     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //
        $user = request()->except('_token', '_method');

        User::where('id', '=', $id)->update($user);
    
        return redirect()->route('home');
    }

